impl Distribution<&'static str> for NameList {
    fn sample<R: Rng + ?Sized>(&self, rng: &mut R) -> &'static str {
        self.choices.choose(rng).unwrap_or(&"")
    }
}

/// Fun `rand::Distribution`s for generating random words.
pub enum WordGen {
    /// Generates a random syllable.
    Syb,
    /// Generates a random real-ish sounding word.
    Word,
    /// Generates a random real-ish sounding word and makes it title-case.
    Title,
    /// Generates a random stock ticker based on the provided string.
    ///
    /// Capital letters will be much more likely to be in the ticker. Vowels are less likely.
    Ticker(String),

}

impl Distribution<String> for WordGen {
    fn sample<R: Rng + ?Sized>(&self, rng: &mut R) -> String {
        match self {
            WordGen::Syb => {
                String::new()
                    + if rng.gen_bool(0.5) {
                    rng.sample(
                        Slice::new(&[
                            "b", "c", "d", "f", "g", "h", "j", "k", "l", "m", "n", "p", "r",
                            "s", "t", "v", "w", "x", "y", "z",
                        ])
                            .unwrap(),
                    )
                } else {
                    ""
                }
                    + rng.sample(Slice::new(&["a", "e", "i", "o", "u"]).unwrap())
                    + rng.sample(
                    Slice::new(&[
                        "b", "d", "f", "g", "h", "k", "l", "m", "n", "p", "r", "s", "t", "v",
                        "x", "z",
                    ])
                        .unwrap(),
                )
            }

            WordGen::Word => {
                let mut chance: f64 = 2.0;
                let mut word = String::new();

                while rng.gen_bool(chance.min(1.0)) {
                    word += &rng.sample(WordGen::Syb);
                    chance /= 2.0;
                }

                word
            }

            WordGen::Ticker(name) => {
                let mut ticker = String::new();

                while ticker.is_empty() {
                    ticker.clear();

                    for c in name.chars() {
                        let mut chance = 4.0 / name.len() as f64;

                        if c.is_uppercase() {
                            chance *= 2.0;
                        } else if ['a', 'e', 'i', 'o', 'u'].contains(&c.to_ascii_lowercase()) {
                            chance /= 2.0
                        }

                        if rng.gen_bool(chance.min(1.0)) {
                            ticker += c.to_string().as_str();
                        }
                    }
                }

                ticker.to_uppercase()
            }
            WordGen::Title => rng.sample(WordGen::Word).to_title_case(),
        }
    }
}
